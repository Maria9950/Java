public class pr1 {

    /**
     * Главный метод программы (точка входа).
     * Демонстрирует использование разных видов комментариев.
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {

        // Однострочный комментарий: создаём переменную и выводим сообщение
        int a = 1;
        System.out.println("Пример работы с комментариями. Значение a = " + a);

        /*
         * Это блочный комментарий.
         * Здесь можно написать разъяснение по коду или описать логику работы.
         */
        a = a + 5;
        System.out.println("После прибавления 5 значение a = " + a);
    }
}
