public class pr1 {
    public static void main(String[] args) {

        // Бесконечный цикл do-while
        int j = 1;
        do {
            System.out.println("do-while: " + j);
            j++;
        } while (true);

        // Бесконечный цикл while
        int k = 1;
        while (true) {
            System.out.println("while: " + k);
            k++;
        }
    }
}
